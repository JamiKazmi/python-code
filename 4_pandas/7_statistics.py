from statistics import mean
from statistics import median
from statistics import mode
from statistics import variance

# returns mean value of the sequence
print(mean([1, 2, 4, 5, 3, 4, 5, 6, 7, 8]))

# returns median of a sequence
print(median([1, 2, 3, 4, 5, 6, 7]))

# returns the mode from sequence
print(mode([1, 2, 4, 5, 3, 5, 6, 7, 8]))

# returns the variance from arithmetic mean
print(variance([1, 2, 3, 4, 5, 6, 7]))
